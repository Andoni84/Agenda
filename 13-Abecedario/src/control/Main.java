package control;

public class Main {

	public static void main(String[] args) {
		
		// bucle while
		char letra = 'a';
		while(letra <= 'z'){
			System.out.print(letra+ ",");
			letra++;
		}
		
		System.out.println("");
		
		//bucle for
		
		for(letra = 'a'; letra <= 'z';letra++){
			System.out.print(letra+",");
		}
		
		System.out.println("");
		
		// bucle do..while
		
		char letra2 = 'a';
		do{
			System.out.print(letra2+",");
			letra2++;
		}while(letra2<='z');
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
